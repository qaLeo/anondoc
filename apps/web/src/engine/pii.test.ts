import { describe, it, expect } from 'vitest'
import { detectPii, deduplicateMatches, findAll } from './piiRules'

function detect(text: string) {
  return deduplicateMatches(detectPii(text))
}

function has(text: string, category: string, original?: string) {
  const matches = detect(text)
  return matches.some(m =>
    m.category === category && (original === undefined || m.original === original),
  )
}

// ── ФИО ───────────────────────────────────────────────────────────────────────

describe('ФИО', () => {
  it('Фамилия Имя Отчество (полное)', () => {
    expect(has('Иванов Иван Иванович', 'ФИО')).toBe(true)
  })

  it('Двойная фамилия', () => {
    expect(has('Иванова-Петрова Мария Сергеевна', 'ФИО')).toBe(true)
  })

  it('Инициалы перед фамилией', () => {
    expect(has('А.С. Пушкин', 'ФИО')).toBe(true)
  })

  it('Инициалы после фамилии', () => {
    expect(has('Пушкин А.С.', 'ФИО')).toBe(true)
  })

  it('Фамилия Имя с контекстом "клиент"', () => {
    expect(has('клиент Петров Алексей', 'ФИО')).toBe(true)
  })

  it('НЕ обнаруживает известные топонимы', () => {
    expect(has('Российская Федерация', 'ФИО')).toBe(false)
    expect(has('Нижний Новгород', 'ФИО')).toBe(false)
  })

  it('НЕ обнаруживает Фамилия Имя без контекста', () => {
    expect(has('Красная Площадь расположена в центре', 'ФИО')).toBe(false)
  })
})

